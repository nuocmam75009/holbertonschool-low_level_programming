#include "3-calc.h"

/**
 * op_add - return the sum of two numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: The sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the difference of two numbers
 * @a: the 1st number
 * @b: the 2ns number
 *
 * Return: The difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the product of two numbers
 * @a: the 1st number
 * @b: the 2ns number
 *
 * Return: The product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the division of two numbers
 * @a: the 1st number
 * @b: the 2ns number
 *
 * Return: The quotient of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the rest of the divison of two numbers
 * @a: the 1st number
 * @b: the 2ns number
 *
 * Return: The rest of division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
